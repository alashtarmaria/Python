# Aritmatik işlemleri :
# Toplama: +
# Çıkarma: -
# Bölme: *
# Üstel: **
# Mod: %
# Tam Sayılı Bölme (Taban): //


a = 9
b = 2

print("Toplam sonucu =", a + b)
print("Çıkarma sonucu =", a - b)
print("Bölme sonucu =" ,a / b)
print("Üstel sonucu =" ,a ** b)
print("Mod sonucu =" ,a % b)
print("Tam Bölme sonucu =" , a // b)

