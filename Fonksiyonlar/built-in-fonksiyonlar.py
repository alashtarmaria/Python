# Python'da built-in (hazır) olan iki alt tire (__) ile erişilen metod veya attribute'lara (özelliklere)
# **dunder** adı verilir.
# `dunder` : `double underscore` 

import math
print(math.pow.__doc__)


# pow fonksiyonu için hangi modülde olduğunu görmek için
print(math.pow.__module__)
print(math.pow.__name__)



